function [x] = gauss(A,b)
n = size(A,1);

for k = 1:n-1
    [~,p] = max(abs(A(k:n,k)));
    p = p + k - 1;
    if A(p,k) == 0
        display('ÆæÒì')
        return 
    end
    if p ~= k
        A([k,p],:) = A([p,k],:);
        b([k,p]) = b([p,k]);
    end
    for i = k+1:n
        m = A(i,k) / A(k,k);
        A(i,k+1:n) = A(i,k+1:n) - m * A(k,k+1:n);
        b(i) = b(i) - m * b(k);
    end
end

if A(n,n) == 0
    display('ÆæÒì')
    return 
end
x = zeros(n,1);
x(n) = b(n) / A(n,n);
for k = n-1:-1:1
    x(k) = (b(k) - A(k,k+1:n) * x(k+1:n)) / A(k,k);
end

end
